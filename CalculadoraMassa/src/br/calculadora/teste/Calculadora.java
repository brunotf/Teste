package br.calculadora.teste;

public class Calculadora {
	public int somar(int a, int b) {
		return a + b;
	}

	public double subtrair(int a, int b) {
		return a - b;
	}

	public double multiplicar(int a, int b) {
		return a * b;
	}

	public double dividir(int a, int b) {
		return a / b;
	}

}
